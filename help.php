    <h2>Welcome to the MemberMap Help Page</h2>
    <h3>How to Register</h3>
    <p>To use the MemberMap system, you must first register an account. On the registration page, you will need to provide:</p>
    <ul>
        <li>Your email address</li>
        <li>Your desired username</li>
        <li>A password</li>
    </ul>
    <p>Once registered, you can log in with your username and password.</p>

    <h3>How to Login</h3>
    <p>If you already have an account, you can log in by visiting the login page and entering your username and password. If you are an admin, you will be redirected to the admin dashboard upon logging in. Regular users will be redirected to the user dashboard.</p>

    <h3>Managing Clubs and Events (Admin Only)</h3>
    <p>Admins have access to manage clubs and events:</p>
    <ul>
        <li><strong>Clubs:</strong> Add and remove clubs, including details like the club name, location, and established date.</li>
        <li><strong>Events:</strong> Add and remove events, including the event title, location, and date.</li>
    </ul>
    <p>To manage clubs and events, use the admin dashboard after logging in as an admin.</p>

    <h3>How to Join a Club (User Only)</h3>
    <p>As a user, you can browse clubs and join them by filling out the membership form. This form includes:</p>
    <ul>
        <li>Start date</li>
        <li>End date</li>
        <li>Membership ID</li>
        <li>Membership fee (500 tk)</li>
        <li>Your phone number</li>
    </ul>
    <p>Once you submit the membership form, you will become a member of the selected club, and it will appear in your dashboard.</p>

    <h3>How to Participate in Events</h3>
    <p>If you are a member of a club, you will be able to view and participate in events hosted by that club. To register for an event, simply navigate to the event page from your dashboard and sign up for any events you are interested in.</p>

    <h3>Logging Out</h3>
    <p>To log out, simply click on the "Logout" link in the navigation bar. This will log you out of your session and return you to the homepage.</p>

    <h3>Need Help?</h3>
    <p>If you encounter any issues or need further assistance, please contact support at <strong>support@membermap.com</strong>.</p>
